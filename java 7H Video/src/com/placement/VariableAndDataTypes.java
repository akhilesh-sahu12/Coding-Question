package com.placement;

public class VariableAndDataTypes {
    public static void main(String args[]){
        // Primitive Data Types - byte, short, int, long
        //                        float, double
        //                        char
        //                        boolean
        float a=8.6f;
        int b=(int)a;
        System.out.println(b);
        char ch='A';
        int c=ch;
        System.out.println(c);//65
        c++;
        System.out.println(c);//66
        char ch1=(char)c;
        System.out.println(ch1);
    }
}
